#include "asm_osx2linux.h"

.text
.globl _get_stack_pointer

_get_stack_pointer:
################################################################################
#if defined(__ppc__) || defined(__PPC__) || defined(__ppc64__)
################################################################################
	addze r3, r1
	blr

################################################################################
#elif __i386__
################################################################################
	movl %esp, %eax
	ret

################################################################################
#elif __x86_64__
################################################################################
	movq %rsp, %rax
	ret

################################################################################
#elif __arm__
################################################################################
	mov r0, sp
	bx lr

################################################################################
#endif // Architectures
################################################################################
